//    Grade System


var marks = prompt("Enter your marks and find your grade");


if (marks >= 90) {
    alert("A Grade")
} 
else if(marks >= 80 ) {
    alert("B Grade")
}
else if(marks >= 70 ) {
    alert("C Grade")
}
else if(marks >= 60 ) {
    alert("D Grade")
}
else if(marks < 60 ) {
    alert("F Grade")
}